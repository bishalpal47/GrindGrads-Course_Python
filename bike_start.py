'''
Question: Write a program to take bike petrol level as input from user. 
If 0, print "Push maar bhai, petrol khatam 😭", else "Chal bhai, ride pe chalte hain 🏍️".
'''

petrol = float(input("Petrol level (litres): "))
if petrol == 0:
    print("Push maar bhai, petrol khatam 😭")
else:
    print("Chal bhai, ride pe chalte hain 🏍️")