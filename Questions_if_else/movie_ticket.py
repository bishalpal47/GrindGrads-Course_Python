'''
Question: Write a program to take age from user. 
If less than 18 or more than 60, print "Discount milega movie ticket mein 🍿", else "Full paisa lagega bhai 😅".
'''


age = int(input("Apni age daal: "))
if age < 18 or age > 60:
    print("Discount milega movie ticket mein 🍿")
else:
    print("Full paisa lagega bhai 😅")