'''
Question: Write a program to take the number of devices connected to the wifi as input from user. If more than 5, print "Bhai wifi to slow hoga ab 🐢. NASA hack kar rahe hai kya sab? Mujhe anime dekhna hai 😥.", else "Speed to badiya aayegi 🚀".
'''

devices = int(input("Kitne devices connected hain wifi se? "))
if devices > 5:
    print("Bhai wifi to slow hoga ab 🐢. NASA hack kar rahe hai kya sab? Mujhe anime dekhna hai 😥.")
else:
    print("Speed to badiya aayegi 🚀. Aaj ka episode aa gaya!")