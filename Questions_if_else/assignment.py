'''
Question: Write a program to ask user whether assignment is done (yes/no). 
If yes, print "Sahi hai bro!! Abhi mera assignment complete kar de 😁", 
else "Itne dino se game khelega to assignment kasie complete hoga rey? Dekh tere chakkar mein mera bhi complete nahi hua 😛"
'''

homework = input("Assignment complete hua tera? (yes/no): ")
if homework.lower() == "yes":
    print("Sahi hai bro!! Abhi mera assignment complete kar de 😁")
else:
    print("Itne dino se game khelega to assignment kasie complete hoga rey? Dekh tere chakkar mein mera bhi complete nahi hua 😛")