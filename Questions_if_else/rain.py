'''
Question: Input whether it's raining (yes/no). If yes, print "Chhata leke ja bhai ☔", else print "Cool, bina chhate style maar sakta hai 😎".
'''

rain = input("Barish ho rahi hai? (yes/no): ")
if rain.lower() == "yes":
    print("Chhata leke ja bhai ☔")
else:
    print("Cool, bina chhate style maar sakta hai 😎")